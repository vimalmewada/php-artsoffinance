    // Get blog list with random shuffle
    public function getBlogList() {
        $shuffledList = $this->BLOG_LIST;
        shuffle($shuffledList);
        return $shuffledList;
    }

    // Get single blog by ID
    public function getBlogById($id) {
        foreach ($this->BLOG_LIST as $blog) {
            if ($blog['id'] == $id) {
                return $blog;
            }
        }
        return null;
    }

    // Get all unique categories
    public function getAllCategories() {
        $allCategories = [];
        foreach ($this->BLOG_LIST as $post) {
            $allCategories = array_merge($allCategories, $post['categories']);
        }
        return array_values(array_unique($allCategories));
    }

    // Get latest blogs sorted by date
    public function getLatestBlogs($limit = 4) {
        $posts = $this->BLOG_LIST;
        
        // Sort by publish date (newest first)
        usort($posts, function($a, $b) {
            return strtotime($b['publishDate']) - strtotime($a['publishDate']);
        });
        
        return array_slice($posts, 0, $limit);
    }

    // Get recent post images
    public function getRecentPostImages($maxCount = 6) {
        $latestPosts = $this->getLatestBlogs($maxCount);
        $images = [];
        
        foreach ($latestPosts as $post) {
            $images[] = $post['featuredImage'];
        }
        
        return $images;
    }

    // Get all unique tags
    public function getAllTags() {
        $allTags = [];
        foreach ($this->BLOG_LIST as $post) {
            $allTags = array_merge($allTags, $post['tags']);
        }
        return array_values(array_unique($allTags));
    }

    // Get blogs by category
    public function getBlogByCategory($category) {
        $filteredBlogs = [];
        $categoryLower = strtolower($category);
        
        foreach ($this->BLOG_LIST as $blog) {
            $blogCategories = array_map('strtolower', $blog['categories']);
            if (in_array($categoryLower, $blogCategories)) {
                $filteredBlogs[] = $blog;
            }
        }
        
        return $filteredBlogs;
    }
